// creating a date in JS 

const d=new Date();

console.log(d); 

console.log("Date is ",d.getDate());
console.log("Day is ",d.getDay());
console.log("Months is ",d.getMonth());
console.log("Year is ",d.getFullYear());
console.log("Second is",d.getSeconds());
console.log("Minute is ",d.getMinutes());
console.log("Hours is",d.getHours());