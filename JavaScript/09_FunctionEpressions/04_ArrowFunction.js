// Example of arrow function
var x=10;
const sqrt=(x) => x*x; // in arrow function we don't need to write return keyword it return the value automatically
console.log("Square of the number is: ",sqrt(x));

// Example of arrow function    
const add=(a,b) => a+b; // arrow function with two parameter
console.log("Addition of two number is: ",add(10,20));
