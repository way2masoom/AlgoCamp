// Function to find number is Odd or even

function isEvenOrOdd(x){
    // X-> Parameter  -> Placeholder -> Expected Input 
    if(x%2==0){
        console.log("Even Number",x);
    }else{
        console.log("Odd number",x);
    }
}
    
// Function call 
isEvenOrOdd(11); // 11 -> argument -> Actual Input
