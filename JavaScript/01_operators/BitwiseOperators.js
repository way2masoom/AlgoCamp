// Bitwise Operators in JS

const a=5;
const b=6;

console.log("Bitwise AND",a&b);
console.log("Bitwise OR",a|b);
console.log("Bitwise XOR",a^b);
console.log("Bitwise NOT",~b);
console.log("Bitwise NOT",~a);

console.log("1 left shift 4 =",1<<4);
console.log("4 left shift 3 =",4<<3);

console.log("27 right shift 2 =",27>>2);
console.log(12>>1);